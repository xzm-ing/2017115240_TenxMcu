//=============================定义按键控制函数================================
//===================任务：实现按键控制led亮灭和蜂鸣器开关===================//
void TaskSetting() {
		if (Keyx == 1){
			if(modeValue == 1) {
				//LED状态转换
				if(D_keyValue1 == keyValue1){
						Key1();		//按下KEY1,同时点亮LED1和LED2
				}
			}
		}
		else if(Keyx == 2){
			if(modeValue == 1) {
				if(D_keyValue1 == keyValue1){
						Key2();		//按下KEY2,点亮LED1
				}
			}
		}			
		else if(Keyx == 3){
			if(modeValue == 1) {
				if(D_keyValue1 == keyValue1){
						Key3();		//按下KEY3,点亮LED2
				}
			}
		}
		else if(Keyx == 4){
			if(modeValue == 1) {
				if(D_keyValue1 == keyValue1){
						Key4();		//按下KEY4，打开蜂鸣器
				}
			}
		}
		else {
			if(modeValue == 1) {
				if(D_keyValue1 == keyValue1){
						modeValue = ~modeValue;			//改变状态
				}
			}
		}
}
