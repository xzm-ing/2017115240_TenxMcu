//===========================定义时间处理函数==================================
//控制时钟
void TimeProcess() {
	static uint8_t timer5ms = 0;		//按键计时

  if (b1ms) {
    // 1ms 执行一次
    b1ms = 0;
    timer5ms++;
  }
	
  if (timer5ms >= 5) {
		timer5ms = 0;
		P1MODL = 0xa8;	//设置IO模式为上拉输入
		P1MODH = 0x2a;
    GetKeys();			//获取按键
  }
}
